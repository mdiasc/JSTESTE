//Switch case

let mesesDoAno=parseInt(prompt("Digite um número para saber o mês"));

switch(mesesDoAno){
    case 1:
        alert("Janeiro")
        break
    case 2:
        alert("Fevereiro")
        break
    case 3:
            alert("Março")
        break
    case 4:
        alert("Abril")
        break
    case 5:
        alert("Maio")
        break
    case 6:
        alert("Junho")
        break
    case 7:
        alert("Julho")
    case 8:
        alert("Agosto")
        break
    case 9:
        alert("Setembro")
        break
    case 10:
        alert("Outubro")
        break
    case 11:
        alert("Novembro")
        break
    case 12:
        alert("Dezembro") 
        default:
        alert("mês inválido")
}

